import { useMemo, useState } from 'react';
import { GetServerSideProps } from 'next';
import Head from 'next/head';
import { DehydratedState, QueryClient, dehydrate, useQuery } from 'react-query';
import styles from '../styles/pages/index.module.css';
import { Pokemon } from '@/interfaces/pokemon.interface';

interface HomeProps {}

// fetcher function: required by react-query
const getPokemons = async () => {
  const response = await fetch(
    'http://jherr-pokemon.s3.us-west-1.amazonaws.com/index.json'
  );
  const json = await response.json();
  return json as Pokemon[];
};

export const getServerSideProps: GetServerSideProps<{
  dehydratedState: DehydratedState;
}> = async () => {
  const queryClient = new QueryClient();
  await queryClient.prefetchQuery('pokemons', getPokemons);
  return { props: { dehydratedState: dehydrate(queryClient) } };
};

export default function Home(props: HomeProps) {
  const { data: pokemons } = useQuery('pokemons', getPokemons, {
    refetchOnMount: false,
    refetchOnWindowFocus: false,
  });

  const [filter, setFilter] = useState('');
  const filteredPokemons = useMemo(() => {
    return !pokemons
      ? []
      : pokemons.filter((pokemon) =>
          pokemon.name.toLowerCase().includes(filter.toLowerCase())
        );
  }, [pokemons, filter]);

  return (
    <div className={styles.main}>
      <Head>
        <title>Pokemon</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.png" />
      </Head>
      <div>
        <input
          type="text"
          value={filter}
          onChange={(e) => setFilter(() => e.target.value)}
          className={styles.search}
        />
      </div>
      <div className={styles.container}>
        {filteredPokemons.slice(0, 20).map((pokemon) => (
          <div key={pokemon.id} className={styles.image}>
            <img
              alt={pokemon.name}
              src={`http://jherr-pokemon.s3.us-west-1.amazonaws.com/${pokemon.image}`}
            />
            <h2>{pokemon.name}</h2>
          </div>
        ))}
      </div>
    </div>
  );
}
